type TranslationKeys = typeof translations;

export function t(section: keyof TranslationKeys, key: string, lang: Language): string {
  const sec = translations[section] as any;
  if (!sec || !sec[key]) return key;
  return sec[key][lang] || sec[key]["en"] || key;
}

export function isRtl(lang: Language): boolean {
  return lang === "ur";
}

